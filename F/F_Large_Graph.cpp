struct DSU
{
    std::vector<int> parent, rank;

    // Constructor to initialize the DSU with `n` elements
    DSU(int n)
    {
        parent.resize(n);
        rank.resize(n, 0);
        for (int i = 0; i < n; ++i)
        {
            parent[i] = i; // Initially, each element is its own parent
        }
    }

    // Find the representative (root) of the set containing `x` with path compression
    int find(int x)
    {
        if (parent[x] != x)
        {
            parent[x] = find(parent[x]); // Path compression
        }
        return parent[x];
    }

    // Union by rank to merge the sets containing `x` and `y`
    void union_sets(int x, int y)
    {
        int rootX = find(x);
        int rootY = find(y);

        if (rootX != rootY)
        {
            // Union by rank
            if (rank[rootX] > rank[rootY])
            {
                parent[rootY] = rootX;
            }
            else if (rank[rootX] < rank[rootY])
            {
                parent[rootX] = rootY;
            }
            else
            {
                parent[rootY] = rootX;
                ++rank[rootX];
            }
        }
    }
    void print_dsu()
    {
        debug(parent);
        debug(rank);
    }
};

